export const VALIDATION_RULES = {
  /** 密码正则（密码格式应为6-18位数字、字母、符号的任意两种组合） */
  password: {
    rule: /^(?![0-9]+$)(?![a-z]+$)(?![A-Z]+$)(?!([^(0-9a-zA-Z)]|[()])+$)(?!^.*[\u4E00-\u9FA5].*$)([^(0-9a-zA-Z)]|[()]|[a-z]|[A-Z]|[0-9]){6,18}$/,
    message: '密码格式应为6-18位数字、字母、符号的任意两种组合'
  },
  verifyCode: {
    rule: /^\d{4}$/,
    message: '验证码错误'
  },
  phone: {
    rule: /^(?:(?:\+|00)86)?1\d{10}$/,
    message: '手机号错误'
  }
}
